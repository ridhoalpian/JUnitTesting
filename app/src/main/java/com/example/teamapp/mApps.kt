package com.example.teamapp // Deklarasi paket (package) tempat kelas mApps berada

class mApps { // Deklarasi sebuah kelas dengan nama mApps
    fun add(first: Int, second: Int): Int { // Deklarasi sebuah fungsi dengan nama add. Fungsi ini menerima dua parameter, yaitu first dan second, keduanya bertipe Int
